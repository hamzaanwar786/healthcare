class ProfileModel {
  String? name;
  String? phone_no;
  String? email;

  get getName => this.name;

  set setName(String? name) => this.name = name;

  get phoneno => this.phone_no;

  set phoneno(value) => this.phone_no = value;

  get getEmail => this.email;

  set setEmail(email) => this.email = email;
}
